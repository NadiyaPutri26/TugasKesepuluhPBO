    public void showTable1() {
        em.clear();
        EntityManager em = Persistence.createEntityManagerFactory("PertemuanKesebelasPU").createEntityManager();
        Query q = em.createNamedQuery("Tokobunga_1.findAll");
        List<Tokobunga_1> hasil = q.getResultList();

        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"ID Bunga", "Nama Bunga", "Kategori", "Deskripsi", "Harga", "Stok", "Ukuran"}, 0
        );
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        for (Tokobunga_1 t : hasil) {
            model.addRow(new Object[]{
                t.getIdbunga(),
                t.getNamabunga(),
                t.getKategoribunga(),
                t.getDeskripsiproduk(),
                t.getHarga(),
                t.getStok(),
                t.getUkuran()
            });
        }
        jTable1.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        jTable1.setModel(model);
        model.fireTableDataChanged();
        jTable1.revalidate();
        jTable1.repaint();
    }

    public void showTable2() {
        try {
            em.clear();
            EntityManager em = Persistence.createEntityManagerFactory("PertemuanKesebelasPU").createEntityManager();
            Query q = em.createNamedQuery("Transaksitokobunga.findAll");
            List<Transaksitokobunga> hasil = q.getResultList();

            tbmk = new DefaultTableModel(
                    new String[]{"ID Transaksi", "ID Bunga", "Nama Pelanggan", "Tanggal Transaksi", "Jumlah Pesanan", "Total Harga"}, 0
            );
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
            for (Transaksitokobunga data : hasil) {
                String tanggalFormatted = "";
                if (data.getTanggal() != null) {
                    tanggalFormatted = sdf.format(data.getTanggal());
                }
                Object[] baris = new Object[6];
                baris[0] = data.getIdtransaksi();
                baris[1] = data.getIdbunga() != null ? data.getIdbunga().getIdbunga() : "";
                baris[2] = data.getNamapelanggan();
                baris[3] = tanggalFormatted;
                baris[4] = data.getJumlah();
                baris[5] = data.getTotalharga();
                tbmk.addRow(baris);
            }

            jTable2.setModel(tbmk);
            jTable2.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

