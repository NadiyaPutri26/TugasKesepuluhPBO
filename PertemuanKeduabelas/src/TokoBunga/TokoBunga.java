    private void BtnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUploadActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PertemuanKesebelasPU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ";";
                while ((baris = br.readLine()) != null) {
                    if (baris.trim().isEmpty()) {
                        continue;
                    }

                    String[] data = baris.split(pemisah);

                    if (data.length < 7) {
                        System.out.println("Baris tidak lengkap, dilewati: " + baris);
                        continue;
                    }

                    String idbunga = data[0].trim();
                    String namabunga = data[1].trim();
                    String kategoribunga = data[2].trim();
                    String deskripsiproduk = data[3].trim();
                    int harga = Integer.parseInt(data[4].trim());
                    int stok = Integer.parseInt(data[5].trim());
                    String ukuran = data[6].trim();

                    if (!idbunga.isEmpty() && !namabunga.isEmpty() && !kategoribunga.isEmpty()
                            && !deskripsiproduk.isEmpty() && !ukuran.isEmpty()) {
                        em.getTransaction().begin();

                        Tokobunga_1 tb = new Tokobunga_1();
                        tb.setIdbunga(idbunga);
                        tb.setNamabunga(namabunga);
                        tb.setKategoribunga(kategoribunga);
                        tb.setDeskripsiproduk(deskripsiproduk);
                        tb.setHarga(harga);
                        tb.setStok(stok);
                        tb.setUkuran(ukuran);

                        em.persist(tb);
                        em.getTransaction().commit();

                        System.out.println("Berhasil input: " + idbunga + " - " + namabunga);
                    } else {
                        System.out.println("Data tidak valid (ada kolom kosong): " + baris);
                    }
                }

                JOptionPane.showMessageDialog(null, "Semua data dari file berhasil diinput!");
                showTable1();

            } catch (Exception ex) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                Logger.getLogger(TokoBunga.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + ex.getMessage());
            } finally {
                if (em.isOpen()) {
                    em.close();
                }
                if (emf.isOpen()) {
                    emf.close();
                }
            }
        }
    }//GEN-LAST:event_BtnUploadActionPerformed

